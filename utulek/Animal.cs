public class Animal
{
    public int id;
    public string name;
    public string type;
    public int age;
    public string gender;
    public string date;
    public string state;
    public string note;

    public Animal(int id, string name, string type, int age, string gender, string date, string state, string note)
    {
        this.id = id;
        this.name = name;
        this.type = type;
        this.age = age;
        this.gender = gender;
        this.date = date;
        this.state = state;
        this.note = note;
    }
}
